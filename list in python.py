
# Creating List in python
list1=[28,10.2,"Ramakant",True]
print(list1)
print(type(list1))

# Using Append Method
list1.append(21)
print(list1)

# Using Remove Method
list1.remove(10.2)
print(list1)

# Using Insert Method
list1.insert(1,"ram")
print(list1)

# Using Extend Method
list2=["hello",False,58]
list1.extend(list2)
print(list1)

# Using Pop Method
list1.pop()
print(list1)
